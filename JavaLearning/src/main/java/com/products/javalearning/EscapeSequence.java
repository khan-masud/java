
package com.products.javalearning;

public class EscapeSequence {
       public static void main(String[] args) {
       System.out.println("Abdullah Al Masudd\b"); // /b will print a backspace. Letter 'b' will delete from 'Masudd'
       System.out.println("\tThe Offline Dreamer"); // \t will create a tap (take 4 whitespace from left)
       System.out.println("Study at : \nDhaka International University"); // \n will print a newline
       System.out.println("CSE, Batch - \"D-93\""); // \" will print a qouble quote
       System.out.println("My Roll Number - \'10\'"); // \' will print a single quote
       System.out.println("I am learning \\Java\\"); // \\ will print a backslash
       
       
   }
}

package com.products.javalearning;

public class DataType {
    public static void main(String[] args) {
        // Basic Data Type and Variables
        int int_number = 10;
        short short_number = 20; // Store small value (-32768 to 32767)
        float float_number = 343.34f; // Must add 'f' after the value
        double double_number = 78263.8237;
        char char_value = 'M';
        boolean bool_value = true;
        
        System.out.println("Integer value : " + int_number);
        System.out.println("Short value : " + short_number);
        System.out.println("Float value : " + float_number);
        System.out.println("Double value : " + double_number);
        System.out.println("Character value : " + char_value);
        System.out.println("Boolean value : " + bool_value);
        
    }
}
